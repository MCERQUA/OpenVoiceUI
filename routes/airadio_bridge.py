# ---------------------------------------------------------------------------
# Library search (dedup) — "is this song already on my AI-Radio?"
# ---------------------------------------------------------------------------


def _title_tokens(s: str):
    import re as _re
    return [t for t in _re.split(r"[^a-z0-9]+", (s or "").lower()) if t]


@airadio_bp.route("/api/airadio/library/search", methods=["GET"])
def library_search():
    """
    Search ONLY the current user's AI-Radio library by title.

    Use this before pushing a song to avoid re-uploading a track the user
    already owns on AI-Radio. The upstream dedupes by `sourceHash`, but the
    agent should still know BEFORE the push whether the song exists so it
    can tell the user instead of silently no-op'ing.

    Query params:
      q     — required, title text (fuzzy-matched)
      limit — optional, default 10, max 50

    Returns:
      {
        ok: true,
        query: "<q>",
        exists: bool,       // any fuzzy match?
        exact: bool,        // true iff at least one match is an exact title
        matches: [
          { id, title, artist, coverUrl, plays, createdAt, ... }
        ]
      }
    """
    if not _bridge_has_auth():
        return _bridge_not_configured_response(for_write=True)

    q = (request.args.get("q") or "").strip()
    if not q:
        return jsonify({
            "ok": False,
            "error": {"code": "VALIDATION_FAILED", "message": "Missing 'q'"},
        }), 400

    try:
        limit = max(1, min(50, int(request.args.get("limit", "10"))))
    except (TypeError, ValueError):
        limit = 10

    print(f"📻 library/search: q='{q}' limit={limit}")

    # Pull the caller's library — the upstream scopes this to the authenticated
    # user automatically (Bearer aia_sk_* or platform key + X-JamBot-User).
    lib = _airadio_call("GET", "/api/agent/library")
    if not lib["ok"]:
        return _bridge_response(lib)

    data = lib.get("data") or {}
    # The library payload shape is { songs: [...], playlists: [...] } — be
    # defensive, the API version still moves.
    songs = data.get("songs") or data.get("items") or []
    if not isinstance(songs, list):
        songs = []

    q_lower = q.lower().strip()
    q_tokens = set(_title_tokens(q))

    matches = []
    exact = False
    for s in songs:
        if not isinstance(s, dict):
            continue
        title = (s.get("title") or "").strip()
        if not title:
            continue
        t_lower = title.lower()
        if t_lower == q_lower:
            exact = True
            matches.append((0, s))
            continue
        if q_lower and q_lower in t_lower:
            matches.append((1, s))
            continue
        t_tokens = set(_title_tokens(title))
        if q_tokens and q_tokens.issubset(t_tokens):
            matches.append((2, s))
            continue
        # Token overlap fallback: all-but-one token match
        if q_tokens and len(q_tokens) > 1:
            overlap = q_tokens & t_tokens
            if len(overlap) >= max(1, len(q_tokens) - 1):
                matches.append((3, s))

    matches.sort(key=lambda pair: pair[0])
    slimmed = []
    for _, s in matches[:limit]:
        slimmed.append({
            "id": s.get("id") or s.get("songId"),
            "title": s.get("title"),
            "artist": s.get("artist") or s.get("artistHandle") or s.get("owner"),
            "coverUrl": s.get("coverUrl") or s.get("cover"),
            "plays": s.get("plays"),
            "createdAt": s.get("createdAt") or s.get("created_at"),
            "isPublic": s.get("isPublic"),
        })

    return jsonify({
        "ok": True,
        "query": q,
        "exists": len(matches) > 0,
        "exact": exact,
        "count": len(matches),
        "matches": slimmed,
    }), 200


