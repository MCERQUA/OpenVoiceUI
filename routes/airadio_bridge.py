def _bridge_has_auth() -> bool:
    """True iff we have a key to authenticate writes (push/vote/friend/etc)."""
    return bool(AIRADIO_URL) and bool(_effective_user_key() or AIRADIO_AGENT_KEY)


def _bridge_has_backend() -> bool:
    """True iff we know where AI-Radio lives — enough for public-only reads."""
    return bool(AIRADIO_URL)


# Back-compat shim — old code calls _bridge_configured(). Reads can proceed in
# public mode; writes still need auth. Caller context determines which matters.
def _bridge_configured() -> bool:
    return _bridge_has_auth()


def _bridge_not_configured_response(for_write: bool = False):
    """503 when env vars are missing. `for_write=True` clarifies the message."""
    missing = []
    if not AIRADIO_URL:
        missing.append("AIRADIO_URL")
    if for_write and not _effective_user_key() and not AIRADIO_AGENT_KEY:
        missing.append(
            "AIRADIO_USER_KEY (generate at ai-radio.jam-bot.com/settings) "
            "or legacy AIRADIO_AGENT_KEY + AIRADIO_JAMBOT_USER"
        )
    msg = (
        "AI-Radio write requires an account — the public browse/play surface "
        "works without one, but push/vote/friend/save need an aia_sk_* key. "
        f"Missing env: {', '.join(missing) or 'unknown'}"
    ) if for_write else (
        "AI-Radio bridge is not configured. "
        f"Missing env: {', '.join(missing) or 'unknown'}"
    )
    return (
        jsonify({
            "ok": False,
            "error": {"code": "BRIDGE_NOT_CONFIGURED", "message": msg},
        }),
        503,
    )


# Paths that are available via /public/* without auth. Each entry maps the
# authenticated /api/agent/<...> path to its public equivalent. If a path is
# NOT in this table, falling back to public mode isn't possible and the
# endpoint will return 503 when no auth is present.
def _public_equivalent(agent_path: str) -> str | None:
    """Return the /public/* path for a given /api/agent/* path, or None."""
    if not agent_path.startswith("/api/agent/"):
        return None
    suffix = agent_path[len("/api/agent/"):]

    # Direct 1:1 mappings
    MAP = {
        "catalog":   "/public/catalog",
        "interpret": "/public/interpret",
    }
    if suffix in MAP:
        return MAP[suffix]

    # Prefix-based mappings
    if suffix.startswith("catalog/"):
        # /api/agent/catalog/<id> → /public/song/<id>
        return f"/public/song/{suffix[len('catalog/'):]}"
    if suffix.startswith("queue"):
        # All /api/agent/queue/* have identical shape at /public/queue/*
        return f"/public/{suffix}"
    if suffix.startswith("u/"):
        # /api/agent/u/<user>           → /public/u/<user>
        # /api/agent/u/<user>/songs     → /public/u/<user>/songs
        # /api/agent/u/<user>/playlists → /public/u/<user>/playlists
        return f"/public/{suffix}"
    if suffix.startswith("playlist/"):
        # /api/agent/playlist/<id> (GET) → /public/playlist/<id>
        return f"/public/{suffix}"
    if suffix.startswith("song/") and suffix.endswith("/stream-url"):
        # No signed-url needed publicly — caller should use /public/stream/<id>.mp3 directly
        song_id = suffix[len("song/"):-len("/stream-url")]
        return f"/public/stream/{song_id}.mp3"

    return None


def _agent_headers() -> dict:
    """
    Auth headers attached to every outbound AI-Radio call.

    Preferred path: Authorization: Bearer aia_sk_* (per-user key from the
    AI-Radio settings page). Identity is embedded in the key.

    Legacy fallback: X-JamBot-Agent-Key + X-JamBot-User (platform-shared
    secret + subdomain-as-user). Retained so existing clients keep working
    during migration.
    """
    eff_key = _effective_user_key()
    if eff_key:
        return {"Authorization": f"Bearer {eff_key}"}
    return {
        "X-JamBot-Agent-Key": AIRADIO_AGENT_KEY,
        "X-JamBot-User": AIRADIO_JAMBOT_USER,
    }


def _airadio_call(method, path, json=None, files=None, params=None, data=None):
    """
    Wrapper around `requests` that targets AI-Radio with the auth headers.

    If the bridge has no auth key but the requested path has a public
    equivalent AND the method is GET, transparently rewrite to /public/* and
    drop the auth headers. This is what makes keyless OVU installs able to
    browse + play without any API key at all. `mode: "public"` is tagged on
    the response so the agent can surface "to upload your own songs, connect
    an account" without guessing.

    Returns a dict shaped:
        {"ok": bool, "data": <json or None>, "error": <dict or None>,
         "status": int, "mode": "auth"|"public"}
    """
    mode = "auth"
    outbound_path = path
    outbound_headers = _agent_headers()

    # Public fallback: only if there's no auth to send and the endpoint has a
    # public equivalent. `_public_equivalent()` ONLY returns mappings for
    # read-safe / inference paths (catalog, song detail, queue, u/<handle>,
    # playlist, interpret, stream). Anything that mutates server state (push,
    # vote, friend, save, set-image, send-to-friend) is never mapped, so
    # writes cannot silently fall through to an unauthenticated call.
    if AIRADIO_PUBLIC_FALLBACK and not _bridge_has_auth():
        pub = _public_equivalent(path)
        if pub:
            outbound_path = pub
            outbound_headers = {}
            mode = "public"

    url = f"{AIRADIO_URL}{outbound_path}"
    try:
        print(f"📻 AI-Radio [{mode}] {method} {outbound_path}")
        resp = http_requests.request(
            method,
            url,
            headers=outbound_headers,
            json=json,
            files=files,
            params=params,
            data=data,
            timeout=AIRADIO_TIMEOUT,
        )
    except http_requests.Timeout as exc:
        logger.warning("AI-Radio timeout: %s %s — %s", method, path, exc)
        return {
            "ok": False,
            "data": None,
            "status": 504,
            "mode": mode,
            "error": {
                "code": "BRIDGE_TIMEOUT",
                "message": f"AI-Radio timed out after {AIRADIO_TIMEOUT}s",
            },
        }
    except http_requests.RequestException as exc:
        logger.warning("AI-Radio request failed: %s %s — %s", method, path, exc)
        return {
            "ok": False,
            "data": None,
            "status": 502,
            "mode": mode,
            "error": {
                "code": "BRIDGE_UNREACHABLE",
                "message": f"Could not reach AI-Radio: {exc}",
            },
        }

    # Try to parse a JSON body — AI-Radio always returns JSON per its contract.
    try:
        body = resp.json()
    except ValueError:
        body = None

    if 200 <= resp.status_code < 300:
        # AI-Radio's own contract is { ok, data?, error? }. Pass it through if
        # present; otherwise treat the parsed body as `data`.
        if isinstance(body, dict) and "ok" in body:
            return {
                "ok": bool(body.get("ok")),
                "data": body.get("data"),
                "error": body.get("error"),
                "status": resp.status_code,
                "mode": mode,
            }
        return {
            "ok": True,
            "data": body,
            "error": None,
            "status": resp.status_code,
            "mode": mode,
        }

    # Non-2xx — propagate AI-Radio's error if it gave us one.
    if isinstance(body, dict) and body.get("error"):
        return {
            "ok": False,
            "data": body.get("data"),
            "error": body.get("error"),
            "status": resp.status_code,
            "mode": mode,
        }
    return {
        "ok": False,
        "data": None,
        "status": resp.status_code,
        "mode": mode,
        "error": {
            "code": "AIRADIO_HTTP_ERROR",
            "message": f"AI-Radio returned HTTP {resp.status_code}",
            "body": (resp.text or "")[:400],
        },
    }


def _bridge_response(result):
    """Convert an `_airadio_call` result into a Flask (json, status) tuple."""
    payload = {
        "ok": result["ok"],
        "data": result.get("data"),
        "error": result.get("error"),
        "mode": result.get("mode", "auth"),
    }
    # Preserve AI-Radio's status code for non-success cases; otherwise 200.
    status = 200 if result["ok"] else (result.get("status") or 502)
    return jsonify(payload), status


def _resolve_local_song(filename: str, playlist: str):
    """
    Look up a local audio track by filename in either the library or the
    generated playlist. Returns (Path, metadata_dict) or (None, None) if
    not found / playlist invalid / path-traversal attempt.
    """
    if not filename:
        return None, None
    if playlist == "generated":
        base_dir = GENERATED_MUSIC_DIR
        meta = load_generated_music_metadata()
    elif playlist == "library":
        base_dir = MUSIC_DIR
        meta = load_music_metadata()
    else:
        return None, None

    # Strict containment — reject `..`, absolute paths, anything not in base.
    safe_name = Path(filename).name
    if safe_name != filename:
        return None, None

    candidate = (base_dir / safe_name).resolve()
    try:
        candidate.relative_to(base_dir.resolve())
    except ValueError:
        return None, None
    if not candidate.is_file():
        return None, None
    return candidate, meta.get(safe_name, {})


def _resolve_image_path(raw_path: str):
    """
    Map a user-supplied image path (URL-style like `/uploads/foo.png` or a
    bare filename) to an absolute Path inside one of the whitelisted dirs.

    Returns Path on success, None on rejection. Strict — no `..`, no absolute
    paths outside the whitelist.
    """
    if not raw_path or not isinstance(raw_path, str):
        return None

    # Reject anything containing `..` outright before anything else.
    if ".." in raw_path:
        return None

    # Reject absolute paths that aren't already inside a whitelisted dir.
    candidate = None
    if raw_path.startswith("/"):
        # First, try matching a known URL prefix.
        for prefix, base in _URL_PREFIX_MAP.items():
            if raw_path.startswith(prefix):
                relative = raw_path[len(prefix):]
                if "/" in relative or "\\" in relative:
                    # No nested subdirs allowed (matches existing /uploads style).
                    return None
                candidate = (base / relative).resolve()
                break
        if candidate is None:
            # An absolute path that doesn't begin with one of our prefixes is
            # never accepted — that would let the agent reach into the host FS.
            return None
    else:
        # Bare filename — assume uploads/ by convention.
        if "/" in raw_path or "\\" in raw_path:
            return None
        candidate = (UPLOADS_DIR / raw_path).resolve()

    if not candidate.is_file():
        return None

    # Final containment check against the whitelist.
    for allowed in _IMAGE_WHITELIST_DIRS:
        try:
            candidate.relative_to(allowed)
            return candidate
        except ValueError:
            continue
    return None


def _stream_song(local_path: Path, fields: dict):
    """
    POST a song file to AI-Radio's /api/agent/push-song as multipart/form-data.

    `fields` is a dict of metadata fields (title, source, etc.) that travel
    alongside the audio bytes. AI-Radio dedupes via the `sourceHash` it
    computes server-side.
    """
    mime, _ = mimetypes.guess_type(str(local_path))
    if not mime:
        mime = "audio/mpeg"
    with open(local_path, "rb") as fh:
        files = {"file": (local_path.name, fh, mime)}
        return _airadio_call(
            "POST",
            "/api/agent/push-song",
            files=files,
            data=fields or None,
        )


def _stream_image(local_path: Path, fields: dict):
    """POST an image file to AI-Radio's /api/agent/set-image."""
    mime, _ = mimetypes.guess_type(str(local_path))
    if not mime:
        mime = "application/octet-stream"
    with open(local_path, "rb") as fh:
        files = {"file": (local_path.name, fh, mime)}
        return _airadio_call(
            "POST",
            "/api/agent/set-image",
            files=files,
            data=fields or None,
        )


