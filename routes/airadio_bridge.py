# ---------------------------------------------------------------------------
# Configuration (env at module load)
# ---------------------------------------------------------------------------

# Default to the public internet URL so standalone OVU installs (no JamBot
# provisioner) can still reach the platform's public catalog without any
# additional config. JamBot clients set AIRADIO_URL=http://ai-radio:3000
# in their compose .env to short-circuit over the shared Docker network.
AIRADIO_URL = os.environ.get("AIRADIO_URL", "https://ai-radio.jam-bot.com").rstrip("/")

# When set, the bridge will answer read-only requests via AI-Radio's
# /public/* endpoints even when no API key is configured. True by default —
# keyless browse and play is the whole point of the public API. Writes
# (push, vote, friend, save, inbox) always require a key regardless.
AIRADIO_PUBLIC_FALLBACK = os.environ.get("AIRADIO_PUBLIC_FALLBACK", "1") not in ("0", "false", "False", "")
# Per-user key (aia_sk_*) takes precedence over the platform-shared agent key
# when set. Generated by the user at ai-radio.jam-bot.com/settings.
AIRADIO_USER_KEY = os.environ.get("AIRADIO_USER_KEY", "")
AIRADIO_AGENT_KEY = os.environ.get("AIRADIO_AGENT_KEY", "")
AIRADIO_JAMBOT_USER = os.environ.get("AIRADIO_JAMBOT_USER", "")
AIRADIO_TIMEOUT = int(os.environ.get("AIRADIO_TIMEOUT", "30"))

# Persistent override for AIRADIO_USER_KEY. Lives in the runtime dir which is
# bind-mounted from the host (/mnt/clients/<user>/openvoiceui/runtime-config),
# so the value survives container restarts and re-images. Written by
# /api/airadio/set-user-key, read at request time to avoid module-reload churn.
_USER_KEY_PERSIST_PATH = RUNTIME_DIR / "airadio-user-key"


def _read_persisted_user_key() -> str:
    """Return the persisted aia_sk_* key, or empty string if none."""
    try:
        if _USER_KEY_PERSIST_PATH.is_file():
            val = _USER_KEY_PERSIST_PATH.read_text().strip()
            if val.startswith("aia_sk_"):
                return val
    except OSError:
        pass
    return ""


def _effective_user_key() -> str:
    """Persisted key wins; env var is the fallback."""
    return _read_persisted_user_key() or AIRADIO_USER_KEY

# ---------------------------------------------------------------------------
# Whitelisted local directories for set-image (security: NO arbitrary paths)
# ---------------------------------------------------------------------------

# Paths the agent is allowed to upload from. Anything outside these is rejected.
# Mirrors the rule in PLAN/18-OVU-INTEGRATION.md §3.3.
_FACES_DIR = RUNTIME_DIR / "faces"
_TEMP_DIR = RUNTIME_DIR / "1temp"

_IMAGE_WHITELIST_DIRS = [
    UPLOADS_DIR.resolve(),
    _FACES_DIR.resolve(),
    KNOWN_FACES_DIR.resolve(),
    GENERATED_MUSIC_DIR.resolve(),
    _TEMP_DIR.resolve(),
]

# URL-prefix → on-disk dir map for paths like "/uploads/foo.png"
_URL_PREFIX_MAP = {
    "/uploads/": UPLOADS_DIR,
    "/faces/": _FACES_DIR,
    "/known_faces/": KNOWN_FACES_DIR,
    "/generated_music/": GENERATED_MUSIC_DIR,
    "/1temp/": _TEMP_DIR,
}

