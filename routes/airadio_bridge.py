def _resolve_song_by_query(query: str, preferred_playlist: str = None):
    """
    Resolve a title/filename query against both playlists (generated first,
    then library). Does exact match on filename first, then fuzzy substring
    match against filename/title. Returns (Path, metadata_dict, playlist_name)
    or (None, None, None).

    Voice tags emit titles like "Ubuntu (You Bun Too)" — not filenames — so
    callers can't know the sanitised on-disk name ("ubuntu-you-bun-too.mp3").
    """
    if not query:
        return None, None, None

    q_norm = query.strip().lower()
    _quote_map = str.maketrans({'\u2018': "'", '\u2019': "'", '\u201c': '"', '\u201d': '"'})
    q_norm = q_norm.translate(_quote_map)

    order = ["generated", "library"]
    if preferred_playlist in order:
        order = [preferred_playlist] + [p for p in order if p != preferred_playlist]

    for pl in order:
        # Exact filename shortcut (preserves existing behavior for callers
        # who already pass a filename).
        path, meta = _resolve_local_song(query, pl)
        if path is not None:
            return path, meta, pl

        # Fuzzy title/filename match against metadata.
        meta_map = (load_generated_music_metadata() if pl == "generated"
                    else load_music_metadata())
        base_dir = GENERATED_MUSIC_DIR if pl == "generated" else MUSIC_DIR

        for fname, m in (meta_map or {}).items():
            t_title = str(m.get("title", "")).lower().translate(_quote_map)
            t_file = fname.lower()
            if q_norm and (q_norm in t_title or q_norm in t_file):
                path, meta2 = _resolve_local_song(fname, pl)
                if path is not None:
                    return path, meta2, pl

    return None, None, None


@airadio_bp.route("/api/airadio/push-song", methods=["POST"])
def push_song():
    """
    Push a single song from MUSIC_DIR (library) or GENERATED_MUSIC_DIR
    (generated) to AI-Radio.

    Body: { "filename": "...", "title": optional track title,
            "playlist": "library"|"generated"|"auto" (default auto),
            "cover_path": optional local image path }

    Either `filename` or `title` must be provided. When `playlist` is
    unspecified or "auto", both playlists are searched (generated first).
    """
    if not _bridge_configured():
        return _bridge_not_configured_response()

    body = request.get_json(silent=True) or {}
    filename = body.get("filename")
    title = body.get("title")
    playlist = body.get("playlist") or "auto"
    cover_path = body.get("cover_path")

    # Voice tags pass a title (not a filename). Fuzzy-resolve across both
    # playlists. Fall back to the legacy exact-filename lookup if the caller
    # explicitly asked for a playlist and passed a filename.
    query = filename or title
    preferred = playlist if playlist in ("generated", "library") else None
    local_path, meta, playlist = _resolve_song_by_query(query, preferred)
    if local_path is None:
        return jsonify({
            "ok": False,
            "error": {
                "code": "NOT_FOUND",
                "message": f"Song '{query}' not found in generated or library playlists",
            },
        }), 404

    fields = {
        "title": title or (meta or {}).get("title") or local_path.stem,
        "source": "ovu-library" if playlist == "library" else "ovu-generated",
        "playlist": playlist,
    }
    # Carry through any useful metadata so AI-Radio doesn't lose it.
    for src_key, dst_key in (
        ("artist", "artist"),
        ("genre", "genre"),
        ("description", "description"),
        ("duration_seconds", "duration"),
        ("suno_id", "sunoId"),
    ):
        if meta and meta.get(src_key) is not None:
            fields[dst_key] = str(meta[src_key])

    # Overlay song-tagger classification when available. Walk the tagger's
    # ranked genre list (CLAP returns top-5) and pick the first one that
    # maps to AI-Radio's 13-slug allowlist. The #1 label is sometimes an
    # un-mappable niche ("bossa nova", "world music", "experimental") —
    # dropping down to #2/#3 usually lands on a valid slug.
    tagger = _load_song_tagger_result(local_path)
    if tagger:
        ranked = ((tagger.get("axes") or {}).get("genre") or [])
        # Fallback when ranked list is empty: try top.genre
        if not ranked:
            top_g = ((tagger.get("top") or {}).get("genre") or "")
            ranked = [{"label": top_g, "score": 1.0}] if top_g else []
        for entry in ranked:
            label = str(entry.get("label", "")).strip().lower()
            mapped = _TAGGER_GENRE_TO_SLUG.get(label)
            if mapped:
                fields["genre"] = mapped
                print(f"🏷️  song-tagger genre: '{label}' → '{mapped}' "
                      f"(score={entry.get('score', 0):.2f})")
                break

    print(f"📻 push-song: {local_path.name} → AI-Radio (playlist={playlist})")
    result = _stream_song(local_path, fields)
    if not result["ok"]:
        return _bridge_response(result)

    # Normalize success shape: { ok, songId, url } as the spec requires,
    # while still passing through anything else AI-Radio gave us.
    data = result.get("data") or {}
    out = {
        "ok": True,
        "songId": data.get("songId") or data.get("id"),
        "url": data.get("url"),
        "data": data,
    }
    return jsonify(out), 200


@airadio_bp.route("/api/airadio/push-playlist", methods=["POST"])
def push_playlist():
    """
    Push a whole playlist to AI-Radio (inline mode — one song per request).

    Body: { "name": "...", "playlist": "library"|"generated",
            "description": optional }
    """
    if not _bridge_configured():
        return _bridge_not_configured_response()

    body = request.get_json(silent=True) or {}
    name = body.get("name")
    playlist = body.get("playlist", "library")
    description = body.get("description", "")

    if not name:
        return jsonify({
            "ok": False,
            "error": {"code": "VALIDATION_FAILED", "message": "Missing 'name' field"},
        }), 400

    if playlist == "generated":
        meta = load_generated_music_metadata()
    elif playlist == "library":
        meta = load_music_metadata()
    else:
        return jsonify({
            "ok": False,
            "error": {"code": "VALIDATION_FAILED", "message": f"Unknown playlist '{playlist}'"},
        }), 400

    # The metadata files are flat {filename: {...}}. There's no concept of a
    # named in-file playlist yet — the social spec says we should look for
    # songs whose metadata mentions the playlist name. For MVP, we treat the
    # playlist arg as "all songs in this directory" if `name` matches a tag,
    # otherwise we send all songs in the dir. AI-Radio dedupes by sourceHash.
    matching_files = []
    name_lower = (name or "").strip().lower()
    for fname, entry in (meta or {}).items():
        playlist_tags = entry.get("playlists") or []
        if isinstance(playlist_tags, str):
            playlist_tags = [playlist_tags]
        tag_match = any(
            isinstance(t, str) and t.lower() == name_lower for t in playlist_tags
        )
        if tag_match:
            matching_files.append(fname)
    if not matching_files:
        # Fallback: the whole directory.
        base_dir = GENERATED_MUSIC_DIR if playlist == "generated" else MUSIC_DIR
        for f in base_dir.iterdir():
            if f.is_file() and f.suffix.lower() in {".mp3", ".wav", ".ogg", ".m4a", ".webm"}:
                matching_files.append(f.name)

    print(f"📻 push-playlist: '{name}' ({playlist}) — {len(matching_files)} song(s)")

    # 1. Create the playlist on AI-Radio first.
    create_payload = {
        "name": name,
        "description": description,
        "source": "ovu-library" if playlist == "library" else "ovu-generated",
    }
    create_result = _airadio_call("POST", "/api/agent/push-playlist", json=create_payload)
    if not create_result["ok"]:
        return _bridge_response(create_result)
    playlist_data = create_result.get("data") or {}
    playlist_id = playlist_data.get("id") or playlist_data.get("playlistId")

    # 2. Push each song inline. Track per-song results so the caller sees
    #    which ones failed without aborting the whole batch.
    pushed = []
    failed = []
    for fname in matching_files:
        local_path, song_meta = _resolve_local_song(fname, playlist)
        if local_path is None:
            failed.append({"filename": fname, "error": "NOT_FOUND"})
            continue
        fields = {
            "title": (song_meta or {}).get("title") or local_path.stem,
            "source": "ovu-library" if playlist == "library" else "ovu-generated",
            "playlist": playlist,
            "playlistId": str(playlist_id) if playlist_id else "",
            "playlistName": name,
        }
        for src_key, dst_key in (
            ("artist", "artist"),
            ("genre", "genre"),
            ("description", "description"),
            ("duration_seconds", "duration"),
            ("suno_id", "sunoId"),
        ):
            if song_meta and song_meta.get(src_key) is not None:
                fields[dst_key] = str(song_meta[src_key])
        song_result = _stream_song(local_path, fields)
        if song_result["ok"]:
            sd = song_result.get("data") or {}
            pushed.append({
                "filename": fname,
                "songId": sd.get("songId") or sd.get("id"),
                "url": sd.get("url"),
            })
        else:
            failed.append({
                "filename": fname,
                "error": (song_result.get("error") or {}).get("code", "PUSH_FAILED"),
                "message": (song_result.get("error") or {}).get("message"),
            })

    return jsonify({
        "ok": True,
        "playlist": playlist_data,
        "pushed_count": len(pushed),
        "failed_count": len(failed),
        "pushed": pushed,
        "failed": failed,
    }), 200


