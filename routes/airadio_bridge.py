def _bridge_configured() -> bool:
    """True iff URL is set AND at least one auth mechanism is available."""
    return bool(AIRADIO_URL) and bool(AIRADIO_USER_KEY or AIRADIO_AGENT_KEY)


def _bridge_not_configured_response():
    """Standard 503 returned when env vars are missing."""
    missing = []
    if not AIRADIO_URL:
        missing.append("AIRADIO_URL")
    if not AIRADIO_USER_KEY and not AIRADIO_AGENT_KEY:
        missing.append("AIRADIO_USER_KEY (or legacy AIRADIO_AGENT_KEY + AIRADIO_JAMBOT_USER)")
    msg = (
        "AI-Radio bridge is not configured. "
        f"Missing env: {', '.join(missing) or 'unknown'}"
    )
    return (
        jsonify({
            "ok": False,
            "error": {"code": "BRIDGE_NOT_CONFIGURED", "message": msg},
        }),
        503,
    )


def _load_user_key_map() -> dict:
    """Return the clerk_uid → { aia_sk, username } map, or {} on any error."""
    try:
        with open(AIRADIO_USER_KEYS_PATH, "r") as f:
            data = json.load(f)
            return data if isinstance(data, dict) else {}
    except FileNotFoundError:
        return {}
    except Exception as exc:
        logger.warning("airadio user-key map load failed: %s", exc)
        return {}


def _resolve_request_user_key() -> str:
    """
    Pick the aia_sk_ key for the current request.

    Priority:
      1. Logged-in Clerk user has a personal key in the user-key map
         (resolves "Mike on bhb's dashboard pushes to Mike's AI-Radio")
      2. Container env AIRADIO_USER_KEY (tenant-scoped aia_sk_ key)
      3. Empty string → caller falls back to legacy AIRADIO_AGENT_KEY path
    """
    try:
        clerk_uid = getattr(flask_g, "clerk_user_id", None)
    except RuntimeError:
        # Not inside a request context (shouldn't happen on real calls)
        clerk_uid = None

    if clerk_uid:
        entry = _load_user_key_map().get(clerk_uid) or {}
        personal_key = (entry.get("aia_sk") or "").strip()
        if personal_key:
            logger.info(
                "airadio: routing to personal key for clerk_uid=%s (user=%s)",
                clerk_uid, entry.get("username") or "?",
            )
            return personal_key

    return AIRADIO_USER_KEY


def _agent_headers() -> dict:
    """
    Auth headers attached to every outbound AI-Radio call.

    Preferred path: Authorization: Bearer aia_sk_* (per-user key).
    The key is resolved per-request so the logged-in Clerk user's personal
    aia_sk_ is used when present, regardless of which tenant subdomain
    they happen to be browsing.

    Legacy fallback: X-JamBot-Agent-Key + X-JamBot-User (platform-shared
    secret + subdomain-as-user). Retained so existing clients keep working
    during migration.
    """
    user_key = _resolve_request_user_key()
    if user_key:
        return {"Authorization": f"Bearer {user_key}"}
    return {
        "X-JamBot-Agent-Key": AIRADIO_AGENT_KEY,
        "X-JamBot-User": AIRADIO_JAMBOT_USER,
    }


def _airadio_call(method, path, json=None, files=None, params=None, data=None):
    """
    Wrapper around `requests` that targets AI-Radio with the auth headers.

    Returns a dict shaped:
        {"ok": bool, "data": <json or None>, "error": <dict or None>, "status": int}

    On non-2xx responses we propagate the AI-Radio error body if it parses as
    JSON, otherwise wrap a synthesized error.
    """
    url = f"{AIRADIO_URL}{path}"
    headers = _agent_headers()
    try:
        print(f"📻 AI-Radio {method} {path}")
        resp = http_requests.request(
            method,
            url,
            headers=headers,
            json=json,
            files=files,
            params=params,
            data=data,
            timeout=AIRADIO_TIMEOUT,
        )
    except http_requests.Timeout as exc:
        logger.warning("AI-Radio timeout: %s %s — %s", method, path, exc)
        return {
            "ok": False,
            "data": None,
            "status": 504,
            "error": {
                "code": "BRIDGE_TIMEOUT",
                "message": f"AI-Radio timed out after {AIRADIO_TIMEOUT}s",
            },
        }
    except http_requests.RequestException as exc:
        logger.warning("AI-Radio request failed: %s %s — %s", method, path, exc)
        return {
            "ok": False,
            "data": None,
            "status": 502,
            "error": {
                "code": "BRIDGE_UNREACHABLE",
                "message": f"Could not reach AI-Radio: {exc}",
            },
        }

    # Try to parse a JSON body — AI-Radio always returns JSON per its contract.
    try:
        body = resp.json()
    except ValueError:
        body = None

    if 200 <= resp.status_code < 300:
        # AI-Radio's own contract is { ok, data?, error? }. Pass it through if
        # present; otherwise treat the parsed body as `data`.
        if isinstance(body, dict) and "ok" in body:
            return {
                "ok": bool(body.get("ok")),
                "data": body.get("data"),
                "error": body.get("error"),
                "status": resp.status_code,
            }
        return {"ok": True, "data": body, "error": None, "status": resp.status_code}

    # Non-2xx — propagate AI-Radio's error if it gave us one.
    if isinstance(body, dict) and body.get("error"):
        return {
            "ok": False,
            "data": body.get("data"),
            "error": body.get("error"),
            "status": resp.status_code,
        }
    return {
        "ok": False,
        "data": None,
        "status": resp.status_code,
        "error": {
            "code": "AIRADIO_HTTP_ERROR",
            "message": f"AI-Radio returned HTTP {resp.status_code}",
            "body": (resp.text or "")[:400],
        },
    }


def _bridge_response(result):
    """Convert an `_airadio_call` result into a Flask (json, status) tuple."""
    payload = {"ok": result["ok"], "data": result.get("data"), "error": result.get("error")}
    # Preserve AI-Radio's status code for non-success cases; otherwise 200.
    status = 200 if result["ok"] else (result.get("status") or 502)
    return jsonify(payload), status


def _resolve_local_song(filename: str, playlist: str):
    """
    Look up a local audio track by filename in either the library or the
    generated playlist. Returns (Path, metadata_dict) or (None, None) if
    not found / playlist invalid / path-traversal attempt.
    """
    if not filename:
        return None, None
    if playlist == "generated":
        base_dir = GENERATED_MUSIC_DIR
        meta = load_generated_music_metadata()
    elif playlist == "library":
        base_dir = MUSIC_DIR
        meta = load_music_metadata()
    else:
        return None, None

    # Strict containment — reject `..`, absolute paths, anything not in base.
    safe_name = Path(filename).name
    if safe_name != filename:
        return None, None

    candidate = (base_dir / safe_name).resolve()
    try:
        candidate.relative_to(base_dir.resolve())
    except ValueError:
        return None, None
    if not candidate.is_file():
        return None, None
    return candidate, meta.get(safe_name, {})


def _resolve_image_path(raw_path: str):
    """
    Map a user-supplied image path (URL-style like `/uploads/foo.png` or a
    bare filename) to an absolute Path inside one of the whitelisted dirs.

    Returns Path on success, None on rejection. Strict — no `..`, no absolute
    paths outside the whitelist.
    """
    if not raw_path or not isinstance(raw_path, str):
        return None

    # Reject anything containing `..` outright before anything else.
    if ".." in raw_path:
        return None

    # Reject absolute paths that aren't already inside a whitelisted dir.
    candidate = None
    if raw_path.startswith("/"):
        # First, try matching a known URL prefix.
        for prefix, base in _URL_PREFIX_MAP.items():
            if raw_path.startswith(prefix):
                relative = raw_path[len(prefix):]
                if "/" in relative or "\\" in relative:
                    # No nested subdirs allowed (matches existing /uploads style).
                    return None
                candidate = (base / relative).resolve()
                break
        if candidate is None:
            # An absolute path that doesn't begin with one of our prefixes is
            # never accepted — that would let the agent reach into the host FS.
            return None
    else:
        # Bare filename — assume uploads/ by convention.
        if "/" in raw_path or "\\" in raw_path:
            return None
        candidate = (UPLOADS_DIR / raw_path).resolve()

    if not candidate.is_file():
        return None

    # Final containment check against the whitelist.
    for allowed in _IMAGE_WHITELIST_DIRS:
        try:
            candidate.relative_to(allowed)
            return candidate
        except ValueError:
            continue
    return None


def _stream_song(local_path: Path, fields: dict):
    """
    POST a song file to AI-Radio's /api/agent/push-song as multipart/form-data.

    AI-Radio expects two specific fields:
      audio : File
      meta  : JSON string { title, artist?, genre?, duration?, isPublic?,
                            sunoId?, sourceHash?, cover? }

    The `fields` dict we receive here is the OVU-side shape (title, source,
    playlist, artist, genre, description, duration, sunoId). We drop the
    OVU-only keys (source, playlist, description) and coerce duration to int.
    """
    mime, _ = mimetypes.guess_type(str(local_path))
    if not mime:
        mime = "audio/mpeg"

    meta = {}
    if fields:
        if fields.get("title"):
            meta["title"] = str(fields["title"])[:200]
        if fields.get("artist"):
            meta["artist"] = str(fields["artist"])[:200]
        raw_genre = str(fields.get("genre") or "").strip().lower()
        # Map common aliases → valid slugs, then drop anything that still
        # isn't in the allowlist. Sending an unknown slug causes P2003.
        _genre_alias = {
            "hip hop": "hip-hop", "hiphop": "hip-hop",
            "lo-fi": "lofi", "lo fi": "lofi",
            "r&b": "rnb", "r and b": "rnb", "rhythm and blues": "rnb",
            "electronica": "electronic", "edm": "electronic",
        }
        raw_genre = _genre_alias.get(raw_genre, raw_genre)
        if raw_genre in _AIRADIO_VALID_GENRES:
            meta["genre"] = raw_genre
        dur = fields.get("duration")
        if dur is not None:
            try:
                meta["duration"] = max(int(float(dur)), 0)
            except (TypeError, ValueError):
                pass
        if fields.get("sunoId"):
            meta["sunoId"] = str(fields["sunoId"])[:200]
        if fields.get("sourceHash"):
            meta["sourceHash"] = str(fields["sourceHash"])[:128]
        if fields.get("isPublic") is not None:
            meta["isPublic"] = bool(fields["isPublic"])
        if fields.get("cover"):
            meta["cover"] = str(fields["cover"])

    # Title is required — fall back to file stem if somehow missing.
    if "title" not in meta:
        meta["title"] = local_path.stem

    with open(local_path, "rb") as fh:
        files = {"audio": (local_path.name, fh, mime)}
        return _airadio_call(
            "POST",
            "/api/agent/push-song",
            files=files,
            data={"meta": json.dumps(meta)},
        )


def _stream_image(local_path: Path, fields: dict):
    """POST an image file to AI-Radio's /api/agent/set-image."""
    mime, _ = mimetypes.guess_type(str(local_path))
    if not mime:
        mime = "application/octet-stream"
    with open(local_path, "rb") as fh:
        files = {"file": (local_path.name, fh, mime)}
        return _airadio_call(
            "POST",
            "/api/agent/set-image",
            files=files,
            data=fields or None,
        )


